package sec01.exam01;

//클래스를 만들뜨는 꼭 앞자리는 대문자로 써라!!!
public class VariableInitalizationExample {

	public static void main(String[] args) {
		
		int value = 30; //value의 초기값을 30으로 설정

		int result = value + 10; //result = 30 + 10 이므로 result에 40을 넣음 
		
		System.out.println(result); //result의 값 출력

	}

}
