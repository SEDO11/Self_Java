package sec08.exam02;

public class Human {
	private String name;
	private int height;
	private int weight;
	
	Human(String name, int height, int weight){
		this.name = name;
		this.height = height;
		this.weight = weight;
	}

	public String getName() {
		return name;
	}

	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}
	
	
}
