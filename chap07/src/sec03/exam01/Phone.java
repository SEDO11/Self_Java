package sec03.exam01;

public abstract class Phone { // 추상 클래스
	//필드
	public String owner; // 소유자 정보 저장 필드
	
	//생성자
	public Phone(String owner) {
		this.owner = owner;
	}
	
	//메서드
	public void turnOn() {
		System.out.println("폰 전원을 켭니다.");
	}
	
	public void turnOff() {
		System.out.println("폰 전원을 끕니다.");
	}
	
}
